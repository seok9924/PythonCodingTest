import sys
from os import path
if path.exists('input.txt'):
    sys.stdin = open("input.txt", "r")



a = input().split('-')
num = []
for i in a:
    cnt = 0
    s = i.split('+')
    for j in s:
        cnt += int(j)
    num.append(cnt)
n = num[0]
for i in range(1, len(num)):
    n -= num[i]
print(n)